package week2.day1;

//creating a class with name FirstCode
public class FirstCode { //class must start and end with curly brackets {}

    //inside a class we create a main method(the starting point of our program)
    public static void main(String[] args) { //main method starts and ends with curly brackets {}

        // println() - shows the message and then makes a newline
        System.out.println("Hello world");

        // print() - just show the message (without newline)
        System.out.print("You see this message below the previous message");

        /*
        This will be printed in the same line,
        because you used print() when printing second message
         */
        System.out.print("But this is in the same line with previous message");

    } //end of a main method
} //end of a class FirstCode